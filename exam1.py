st1 = "He likes apples, she likes too"
print(st1)
print('-' * 20)

# 문자열을 대문자로
st2 = st1.upper()
print(st2)
print('-' * 20)
# 문자열을 소문자로
st2 = st1.lower()
print(st2)
print('-' * 20)
# 대문자는 소문자로, 소문자는 대문자로
st2 = st1.swapcase()
print(st2)
print('-' * 20)
# 문자열의 첫글자만 대문자로
st3 = st2.capitalize()
print(st3)
print('-' * 20)
# 단어의 첫글자만 대문자로
st2 = st1.title()
print(st2)
print('-' * 20)
